// 단어 출력
#include <iostream>
using namespace std;

int main() {
    // cout은 <<, cin은 >> 방향
    cout << "Hello";
    return 0;
}

// 문장 출력
#include <iostream>
using namespace std;

int main() {
    cout << "Hello World!";
    return 0;
}

// 특수문자 출력은 \ + 특수문자
#include <iostream>
using namespace std;

int main() {
    cout << "He says \"It's a really simple sentence\".";
    return 0;
}

// 줄바꿈은 endl 또는 "\n"
#include <iostream>
using namespace std;

int main() {
    // cout << "Hello\nWorld";
    cout << "Hello" << endl << "World";
    return 0;
}

// 숫자는 그냥 숫자 3 또는 문자열 "3" 동일
#include <iostream>
using namespace std;

int main() {
    cout << 3;
    return 0;
}

// 사이에 공백을 넣을 때는 이렇게 
#include <iostream>
using namespace std;

int main() {
    // cout << "3 5";
    cout << 3 << " " << 5;
    return 0;
}

